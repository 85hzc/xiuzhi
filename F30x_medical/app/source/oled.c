/*!
    \brief      oled init function
    \param[in]  none
    \param[out] none
    \retval     none
*/
void oled_init(void)
{
    oled_write_byte(0xAE,OLED_CMD);                 /* turn off oled panel */

    oled_write_byte(0x81,OLED_CMD);                 /* set contrast control register */
    oled_write_byte(BRIGHTNESS,OLED_CMD);           /* set seg output brightness, 0x00-0xff */

    oled_write_byte(0xA0,OLED_CMD);                 /* set seg/column mapping, column address 127 is mapped to seg0 */
    oled_write_byte(0xC0,OLED_CMD);                 /* set com/row scan direction, scan from com[n-1] to com0 */

    oled_write_byte(0xD5,OLED_CMD);                 /* set display clock divide ratio/oscillator frequency */
    oled_write_byte(0x80,OLED_CMD);                 /* divide ratio is 1, use the default internal clock */
    oled_write_byte(0xA8,OLED_CMD);                 /* set multiplex ratio, 0x0f-0x3f */
    oled_write_byte(0x3F,OLED_CMD);                 /* 1/64 duty */

    oled_write_byte(0xD3,OLED_CMD);                 /* set vertical shift by com from 0d~63d */
    oled_write_byte(0x00,OLED_CMD);                 /* not offset */

    oled_write_byte(0xD9,OLED_CMD);                 /* set pre-charge period */
    oled_write_byte(0xF1,OLED_CMD);                 /* set pre-charge as 15 clocks & discharge as 1 clock */

    oled_write_byte(0xDA,OLED_CMD);                 /* set com pins hardware configuration */
    oled_write_byte(0x12,OLED_CMD);                 /* sequential com pin configuration and disable com left/right remap */

    oled_write_byte(0xDB,OLED_CMD);                 /* adjusts the Vcomh regulator output */
    oled_write_byte(0x30,OLED_CMD);                 /* Set Vcom to 0.83Vcc */

    oled_write_byte(0x20,OLED_CMD);                 /* set memory addressing mode, 0x00-0x02 */
    oled_write_byte(0x02,OLED_CMD);                 /* page addressing mode */

    oled_write_byte(0x8D,OLED_CMD);                 /* charge pump setting */
    oled_write_byte(0x14,OLED_CMD);                 /* enable charge pump */

    oled_write_byte(0xA4,OLED_CMD);                 /* disable entire display on, 0xA4 or 0xA5 */

    oled_write_byte(0xA6,OLED_CMD);                 /* set normal display, 0 is OFF display and 1 is ON display */

    oled_write_byte(0xAF,OLED_CMD);                 /* normal mode or sleep mode, 0xAF or 0xAE */

    oled_clear(0, 8);
}


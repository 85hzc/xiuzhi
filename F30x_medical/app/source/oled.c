/*!
    \brief      initialize the GPIO port of oled module
    \param[in]  none
    \param[out] none
    \retval     none
*/
void oled_io_init(void)
{
    /* enable the clock of GPIO */
    rcu_periph_clock_enable(OLED_SA0_GPIO_CLK);
    rcu_periph_clock_enable(OLED_CS_GPIO_CLK);

    gpio_init(OLED_SA0_GPIO_PORT, GPIO_MODE_OUT_PP, GPIO_OSPEED_2MHZ, OLED_SA0_PIN);
    gpio_init(OLED_CS_GPIO_PORT, GPIO_MODE_OUT_PP, GPIO_OSPEED_2MHZ, OLED_CS_PIN);
    
    /* select oled */
    gpio_bit_reset(OLED_CS_GPIO_PORT, OLED_CS_PIN);

    //i2c slave address set
    gpio_bit_reset(OLED_SA0_GPIO_PORT, OLED_SA0_PIN);
}

